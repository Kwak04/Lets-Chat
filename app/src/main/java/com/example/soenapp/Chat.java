public class Chat {
    String text;
    String person;
    String time;
    String user_key;

    @Override
    public String toString() {
        return "Chat{" +
                "text='" + text + '\'' +
                ", person='" + person + '\'' +
                ", time='" + time + '\'' +
                ", user_key='" + user_key + '\'' +
                '}';
    }
}



